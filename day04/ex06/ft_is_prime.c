int		ft_is_prime(int nb);
int		ft_is_prime(int nb)
{
	int n;

    if (nb % 2 == 0 && nb != 2)
    {
    	return 0;
    }
    while (n++ < nb)
    {
    	if(nb % n == 0){
    		return 0;
    	}
    }
    return 1;
}